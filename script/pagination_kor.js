const listEl = document.getElementById("list");
const paginationEl = document.getElementById("pagination");

let currentPage = 1;
let rows = 10;

function displayList(items, wrapper, rows_per_page, page) {
  wrapper.innerHTML = "";
  page--;

  let start = rows_per_page * page;
  let end = start + rows_per_page;
  let paginatedItems = items.slice(start, end);

  for (let i = 0; i < paginatedItems.length; i++) {
    let item = `<article>
			<a href="${paginatedItems[i].link}" target="_blank">
				<h2 class="title-post">${paginatedItems[i].title}</h2>
			</a>
			<div class="info">
				<div>
					<span class="date">${paginatedItems[i].date}</span>
					<span class="lang">${paginatedItems[i].lang}</span>
				</div>
				<div class="media">${paginatedItems[i].media}</div>
			</div>
		</article>`;

    wrapper.insertAdjacentHTML("beforeend", item);
  }
}
function setupPagination(items, wrapper, rows_per_page) {
  wrapper.innerHTML = "";
  // console.log(items);
  let pageCount = Math.ceil(items.length / rows_per_page);
  for (let i = 1; i < pageCount + 1; i++) {
    let btn = paginationButton(i, items);
    wrapper.appendChild(btn);
  }
}
function paginationButton(page, items) {
  let button = document.createElement("button");
  button.innerText = page;

  if (currentPage == page) button.classList.add("active");

  button.addEventListener("click", () => {
    currentPage = page;
    displayList(items, listEl, rows, currentPage);

    let currentBtn = document.querySelector(".pagenumbers button.active");
    currentBtn.classList.remove("active");

    button.classList.add("active");
  });

  return button;
}

displayList(listItem, listEl, rows, currentPage);
setupPagination(listItem, paginationEl, rows);
